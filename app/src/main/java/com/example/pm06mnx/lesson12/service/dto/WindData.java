package com.example.pm06mnx.lesson12.service.dto;

/**
 * Основные данные по погоде
 */
public class WindData {

    private Float speed;
    private Float deg;

    public Float getSpeed() {
        return speed;
    }

    public void setSpeed(Float speed) {
        this.speed = speed;
    }

    public Float getDeg() {
        return deg;
    }

    public void setDeg(Float deg) {
        this.deg = deg;
    }
}
